package variable;

public class var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32,768 ~ 21,767
        int i = 2147483647; // -2147483648 ~ 2147483647

//        -9223372036854775808L ~ 9223372036854775807L
        long l = 9223372036854775807L; //L 붙여줘야함

        // 실수
        float f = 10.0f; // f 붙여줘야함
        double d = 10.0; // 더 큰 범위
    }
}
